def binary_search(data: list, target: any, low_position: int, high_position: int):
    if low_position > high_position:
        return False

    mid_position = (low_position + high_position) // 2
    if data[mid_position] == target:
        return mid_position
    if data[mid_position] > target:
        return binary_search(data, target, low_position, mid_position - 1)
    return binary_search(data, target, mid_position + 1, high_position)


